package HomeWork;

public class D11HWLoop1 {
	
	public static void main(String[] args) {
		
		
		for(int y=-24; y<-23; y--) {
			System.out.println(y);
		}
		
		for(int aiza=777; aiza>666; aiza++) {
			System.out.println(aiza);
		}
		
		for(int time=24; time<365; ) {
			System.out.print(time);
			time--;
		}
	}

}
