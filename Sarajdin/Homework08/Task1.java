package Homework08;

//Task1--> Three infinite for loops
public class Task1 {
	public static void main(String [] args) {
		
		//First infinite for loop.
		for(int i =0; i>=0; i++) {
			System.out.println("infinite for loop 1");
		}
		
		
		//Second infinite for loop.
		for(int i=0; i<10; i--) {
			System.out.println("infinite for loop 2");
		}
		
		//Third infinite for loop.
		for(int i=5; i>3; i++) {
			System.out.println("infinite for loop 3");
		}
		
	}

}
